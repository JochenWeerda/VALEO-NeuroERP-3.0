// History Component
function OpportunityHistoryTab({ opportunityId }: { opportunityId: string }) {
  const { t } = useTranslation()
  const [history, setHistory] = useState<any[]>([])
  const [loading, setLoading] = useState(true)

  useEffect(() => {
    const loadHistory = async () => {
      try {
        const response = await apiClient.get(`/opportunities/${opportunityId}/history`)
        if (response.success) {
          setHistory(response.data || [])
        }
      } catch (error) {
        console.error('Fehler beim Laden der History:', error)
      } finally {
        setLoading(false)
      }
    }
    if (opportunityId) {
      loadHistory()
    }
  }, [opportunityId])

  if (loading) {
    return <div className="p-4">Lade History...</div>
  }

  if (history.length === 0) {
    return <div className="p-4 text-muted-foreground">{t('crud.messages.noHistory')}</div>
  }

  return (
    <div className="space-y-4">
      {history.map((entry) => (
        <Card key={entry.id}>
          <CardContent className="pt-4">
            <div className="flex items-start justify-between">
              <div className="flex-1">
                <div className="font-medium">{entry.field_name}</div>
                <div className="text-sm text-muted-foreground mt-1">
                  {entry.old_value && (
                    <span className="line-through text-red-600">{entry.old_value}</span>
                  )}
                  {entry.old_value && entry.new_value && ' → '}
                  {entry.new_value && (
                    <span className="text-green-600">{entry.new_value}</span>
                  )}
                </div>
                {entry.change_reason && (
                  <div className="text-xs text-muted-foreground mt-1">
                    {t('crud.fields.reason')}: {entry.change_reason}
                  </div>
                )}
              </div>
              <div className="text-right text-sm text-muted-foreground">
                <div>{entry.changed_by}</div>
                <div>{formatDate(entry.changed_at)}</div>
              </div>
            </div>
          </CardContent>
        </Card>
      ))}
    </div>
  )
}

