export default function OpportunityDetailPage(): JSX.Element {
  const { t } = useTranslation()
  const navigate = useNavigate()
  const { id } = useParams<{ id: string }>()
  const [loading, setLoading] = useState(false)
  const [activeTab, setActiveTab] = useState('grundinformationen')
  const entityType = 'opportunity'
  const entityTypeLabel = getEntityTypeLabel(t, entityType, 'Opportunity')
  const opportunityConfig = createOpportunityConfig(t, entityTypeLabel)
  const isNew = !id || id === 'neu' || id === 'new'

  const { data, saveData, isLoading: dataLoading } = useMaskData({
    apiUrl: opportunityConfig.api.baseUrl,
    id: id || undefined
  })

  const { validate } = useMaskValidation({
    schema: createOpportunitySchema(t)
  })

  const handleSave = async (formData: any) => {
    setLoading(true)
    try {
      // Calculate expected_revenue if amount and probability are provided
      if (formData.amount && formData.probability) {
        formData.expected_revenue = formData.amount * (formData.probability / 100)
      }

      // Set owner_id from assigned_to if not provided
      if (!formData.owner_id && formData.assigned_to) {
        formData.owner_id = formData.assigned_to
      }

      // Validate
      const validationResult = validate(formData)
      if (!validationResult.valid) {
        toast({
          variant: 'destructive',
          title: t('crud.messages.validationError'),
          description: validationResult.errors.join(', ')
        })
        return
      }

      await saveData(formData)
      toast({
        title: getSuccessMessage(t, isNew ? 'create' : 'update', entityType),
      })
      navigate('/crm/opportunities')
    } catch (error: any) {
      console.error('Save error:', error)
      toast({
        variant: 'destructive',
        title: getErrorMessage(t, isNew ? 'create' : 'update', entityType),
        description: error.message || t('crud.messages.unknownError')
      })
    } finally {
      setLoading(false)
    }
  }

  const handleCancel = () => {
    if (confirm(t('crud.messages.discardChanges'))) {
      navigate('/crm/opportunities')
    }
  }

  const handleAction = async (action: string, formData: any) => {
    if (action === 'convertToQuote') {
      // TODO: Implement convert to quote
      toast({
        title: t('crud.messages.comingSoon'),
        description: t('crud.messages.convertToQuoteComingSoon')
      })
    } else if (action === 'markAsWon' && id) {
      try {
        await apiClient.put(`/opportunities/${id}`, {
          status: 'closed_won',
          actual_close_date: new Date().toISOString()
        })
        toast({
          title: getSuccessMessage(t, 'update', entityType),
        })
        navigate('/crm/opportunities')
      } catch (error) {
        toast({
          variant: 'destructive',
          title: getErrorMessage(t, 'update', entityType)
        })
      }
    } else if (action === 'markAsLost' && id) {
      try {
        await apiClient.put(`/opportunities/${id}`, {
          status: 'closed_lost'
        })
        toast({
          title: getSuccessMessage(t, 'update', entityType),
        })
        navigate('/crm/opportunities')
      } catch (error) {
        toast({
          variant: 'destructive',
          title: getErrorMessage(t, 'update', entityType)
        })
      }
    }
  }

  if (dataLoading && !isNew) {
    return (
      <div className="flex items-center justify-center p-8">
        <div className="text-center">
          <div className="animate-spin rounded-full h-8 w-8 border-b-2 border-primary mx-auto mb-2"></div>
          <p>{t('crud.messages.loading')}</p>
        </div>
      </div>
    )
  }

  return (
    <div className="space-y-4">
      <div className="flex items-center justify-between">
        <div>
          <Button variant="ghost" onClick={() => navigate('/crm/opportunities')} className="mb-2">
            <ArrowLeft className="h-4 w-4 mr-2" />
            {t('crud.actions.back')}
          </Button>
          <h1 className="text-3xl font-bold">
            {isNew 
              ? t('crud.actions.create', { entityType: entityTypeLabel })
              : getDetailTitle(t, entityTypeLabel, data?.number || id || '')
            }
          </h1>
        </div>
      </div>

      <div className="grid grid-cols-4 gap-4">
        <div className="col-span-3">
          <ObjectPage
            config={opportunityConfig}
            data={data}
            onSave={handleSave}
            onCancel={handleCancel}
            onAction={handleAction}
            isLoading={loading || dataLoading}
          />
        </div>

        {!isNew && id && (
          <div className="col-span-1 space-y-4">
            <Card>
              <CardHeader>
                <CardTitle className="flex items-center gap-2">
                  <History className="h-5 w-5" />
                  {t('crud.detail.history')}
                </CardTitle>
              </CardHeader>
              <CardContent>
                <OpportunityHistoryTab opportunityId={id} />
              </CardContent>
            </Card>

            <Card>
              <CardHeader>
                <CardTitle className="flex items-center gap-2">
                  <FileText className="h-5 w-5" />
                  {t('crud.detail.quotes')}
                </CardTitle>
              </CardHeader>
              <CardContent>
                <OpportunityQuotesTab opportunityId={id} />
              </CardContent>
            </Card>
          </div>
        )}
      </div>
    </div>
  )
}

